#include <stdio.h>

// Assignment operators are used to assign values to variables.

int main() {

     /* -------- ASSIGNMENT OPERATOR -------- */
 
   // addition assignment operator (+=) adds a value to a variable

   // Defining an Integer
   int x = 10;
   
    // Addition Assignment operator (+=)  => First definition that x=10 but than you add the assigning value of 5 - 10+5=15
    x += 5;
    
    // Print the Assignment Operator Solution
    printf("%d", x);

    
     /* -------- ALL ASSIGNMENT OPERATORS -------- */
   
     /*
     *
     *   "="   ->  x=10    
     *   "+="  ->  x=10+5
     *   "-="  ->  x=10-5
     *   "*="  ->  x=10*5
     *   "/="  ->  x=10/5
     *   "%="  ->  x=x%5 [Modulus]
     *   "|="  ->  x=x|5
     *   "^="  ->  x=x^5
     *   ">>="  ->  x=x>>5
     *   "<<="  ->  x=x<<5
     * 
     */


    return 0;


}