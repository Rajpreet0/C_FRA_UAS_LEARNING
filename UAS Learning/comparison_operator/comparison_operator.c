#include <stdio.h>

// Comparison operators are used to compare two values
// The return value of a comparison is either true (1) or false (0)

int main(){
  
   /* -------- COMPARISON OPERATOR -------- */

   // greater than operator (>) to find out if the value x is greater than y

   // Defining x and y 
   int x = 5;
   int y = 3;

   // Print if the comparison operator
   // Solution: Returns 1 [true] because 5 is greater than 3 
   printf("%d", x > y); 

   /* -------- ALL COMPARISON OPERATORS -------- */

   /*
   *
   * "=="  -> Equal to
   * "!="  -> Not Equal
   * ">"   -> Greater than
   * "<"   -> Less than
   * ">="  -> Greater than or equal to
   * "<="  -> Less than or equal to
   * 
   */

    return 0;
}